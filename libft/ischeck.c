#include "libft.h"

int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ? 1 : 0);
}

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) != 0 || ft_isdigit(c) != 0)
		return (1);
	return (0);
}

int	ft_isascii(int c)
{
	return ((c >= 0 && c <= 127) ? 1 : 0);
}

int	ft_isprint(int c)
{
	return ((c >= 32 && c <= 126) ? 1 : 0);
}

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}
